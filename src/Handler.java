    public void render(Graphics g) {
        g.setColor(Color.BLACK);
        g.fillRect(0, 0, width, height);


        int[][] tempRoom = rooms.get(map[roomPos[1]][roomPos[0]]);



        for (int i = 0; i < W; i++){
            for (int j = 0; j < H; j++){
                if (tempRoom[j][i] == 0){
                    g.setColor(Color.GRAY);
                    g.drawRect(i * (width / W), j * (height / H), (width / W), (height / H));
                } else {
                    g.setColor(new Color( 170, 170, 170));
                    g.fillRect(i * (width / W), j * (height / H), (width / W), (height / H));
                }
            }
        }

        g.setColor(player.getColor());
        g.fillOval(player.getX() * (width / W) + ((width / W) - SIZE)/2, player.getY()  * (height / H) + ((height / H) - SIZE)/2, SIZE, SIZE);


    }

