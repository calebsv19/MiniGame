    public void moveRight(){
        if (player.getX() == W - 1){
            roomPos[0] = roomPos[0] + 1;
            currentRoom = rooms.get(map[roomPos[1]][roomPos[0]]);
            player.setX(0);
        } else if (player.getX() < W - 1){
            if (currentRoom[player.getY()][player.getX() + 1] == 0){
                player.setX(player.getX() + 1);
            }
        }
    }
    public void moveLeft(){
        if (player.getX() == 0){
            roomPos[0] = roomPos[0] - 1;
            currentRoom = rooms.get(map[roomPos[1]][roomPos[0]]);
            player.setX(W - 1);
        } else if (player.getX() > 0){
            if (currentRoom[player.getY()][player.getX() - 1] == 0){
                player.setX(player.getX() - 1);
            }
        }
    }
    public void moveUp(){
        if (player.getY() == 0){
            roomPos[1] = roomPos[1] - 1;
            currentRoom = rooms.get(map[roomPos[1]][roomPos[0]]);
            player.setY(H - 1);
        } else if (player.getY() > 0){
            if (currentRoom[player.getY() - 1][player.getX()] == 0){
                player.setY(player.getY() - 1);
            }
        }
    }
    public void moveDown(){
        if (player.getY() == H - 1){
            roomPos[1] = roomPos[1] + 1;
            currentRoom = rooms.get(map[roomPos[1]][roomPos[0]]);
            player.setY(0);
        } else if (player.getY() < H - 1){
            if (currentRoom[player.getY() + 1][player.getX()] == 0){
                player.setY(player.getY() + 1);
            }
        }
    }
